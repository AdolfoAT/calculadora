    private void retrocesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retrocesoActionPerformed
        // TODO add your handling code here:
        String num1 = pantalla.getText();
        int longitud = num1.length();
        if (longitud==1){
            pantalla.setText("");
            numText="";
        }
        else{
        char arregloNumeros []= num1.toCharArray();
        char modificado [] = new char [arregloNumeros.length-1];
        System.arraycopy(arregloNumeros, 0, modificado, 0, arregloNumeros.length-1);
        String auxText =new String (modificado);
        pantalla.setText(auxText);
        numText=auxText;
        }
    }//GEN-LAST:event_retrocesoActionPerformed

