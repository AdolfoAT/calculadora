   public void operar(){
       num=resultado;
        switch(operacion){
            case "+":
                resultado = num+num2;
                break;
            case "-":    
                resultado = num-num2;
                break;
            case "*":           
                resultado = num*num2;
                break;
            case "/":          
                resultado = num/num2;
                break;
                
        }
   }
